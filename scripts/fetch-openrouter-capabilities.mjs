#!/usr/bin/env node
// Fetch OpenRouter's model catalog and emit a thin capabilities JSON.
//
// We only care about input modalities for the attach-button gate — does
// the model accept image inputs (and, in the future, native PDF). The
// regex heuristic in main.ts gets the common cases right but lags
// reality (new model line ships → user can't attach until heuristic
// updated). This bakes a snapshot of the truth from
// https://openrouter.ai/api/v1/models so the gate is data-driven.
//
// Output: src/data/modelModalities.ts — TS module exporting a Record
//   { "anthropic/claude-haiku-4.5": ["text", "image"], ... }
// Emitted as a TS module (not raw JSON) so the no-bundle build pipeline
// (per-file esbuild, browser fetches each .mjs) can pull it in via a
// normal `import` without JSON-loader plumbing.
//
// Run on demand:
//   node scripts/fetch-openrouter-capabilities.mjs
//
// Local-only models (gemma running on hermes via llama.cpp, etc.) are
// NOT in OpenRouter's catalog. The PWA's resolver falls back to a
// minimal local-models map for those.

import { writeFileSync, mkdirSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT_PATH = path.join(__dirname, '..', 'src', 'data', 'modelModalities.ts');

async function main() {
  process.stdout.write('Fetching OpenRouter model catalog... ');
  const res = await fetch('https://openrouter.ai/api/v1/models', {
    headers: { 'Accept': 'application/json' },
  });
  if (!res.ok) {
    console.error(`\nfailed: HTTP ${res.status}`);
    process.exit(1);
  }
  const catalog = await res.json();
  const models = Array.isArray(catalog?.data) ? catalog.data : [];
  console.log(`got ${models.length} models`);

  // Slim shape: id → input_modalities[]. Drop anything text-only since
  // the only consumer is the attach-button vision gate; text-only
  // models default to "no attachments" without needing an entry.
  const out = {};
  let kept = 0;
  for (const m of models) {
    const id = m?.id;
    const mods = m?.architecture?.input_modalities;
    if (!id || !Array.isArray(mods)) continue;
    const interesting = mods.filter(mod => mod !== 'text');
    if (interesting.length === 0) continue;
    out[id] = mods;
    kept++;
  }
  console.log(`keeping ${kept} models with non-text input modalities`);

  mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  const fetchedAt = new Date().toISOString();
  const banner = [
    `// AUTO-GENERATED by scripts/fetch-openrouter-capabilities.mjs.`,
    `// Do NOT edit by hand — re-run the script to refresh.`,
    `// Source: https://openrouter.ai/api/v1/models`,
    `// Fetched: ${fetchedAt}`,
    `// Only models with non-text input modalities are included; missing`,
    `// IDs default to text-only at the call site.`,
    ``,
  ].join('\n');
  const body =
    `export const FETCHED_AT = '${fetchedAt}';\n\n` +
    `export const MODEL_MODALITIES: Record<string, readonly string[]> = ${JSON.stringify(out, null, 2)};\n`;
  writeFileSync(OUT_PATH, banner + body);
  console.log(`wrote ${OUT_PATH}`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
